package Clinique;

public class patient {
	
	private String name;
	private int p_id;
	private long mob_no;
	private int age;
	public String getName() {
		return name;
	}
	public int getP_id() {
		return p_id;
	}
	public long getMob_no() {
		return mob_no;
	}
	public int getAge() {
		return age;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setP_id(int p_id) {
		this.p_id = p_id;
	}
	public void setMob_no(long mob_no) {
		this.mob_no = mob_no;
	}
	public void setAge(int age) {
		this.age = age;
	}
	

}
